            <p>Many students travel to and from the various campuses by public transport. You can get to the <a href="http://www.victoria.ac.nz/home/about/maps/campus-kelburn">Kelburn Campus</a> from the city centre by bus or cable car, which runs up and down between Kelburn and Lambton Quay every 10 minutes.</p>
                                    
