            <h4>Free Bus Passes</h4>
            
            <p>VUWSA provides free bus passes to students travelling between campuses. These are available from VUWSA's Kelburn, Pipitea and Karori Offices. All current students are entitled to a free bus pass, providing they meet the below requirements.</p>
            
            <p><strong>Undergraduate students:</strong></p>
            
            <p>You will need to log on to Student Records on <a href="https://my.vuw.ac.nz/cp/home/displaylogin">myVictoria</a> and print off and present the following documentation each time you need a bus ticket.</p>
            
            <p>Current study timetable which shows what classes you are doing, at which campus and on what days</p>
            
            <p>Your tutorial listing. This can be found on S-Cubed under My Signups. Like the study timetable, this tutorial listing will indicate what days your tutorials are along with their venues and times.</p>
            
            <p>Your Student ID.</p>
            
            <p>You are only entitled to a bus ticket if you have the above document(s) and have classes or tutorials on two different campuses on the same day. If you have classes or tutorials on separate days, you are not entitled to a ticket.</p>
            
            <p>To obtain a new bus ticket you need to keep your used ticket and return it once it has been all clipped out. Bring it back along with the documents outlined in 1, 2 and 3 above.</p>
            
            <p><strong>Postgraduate students:</strong></p>
            
            <p>You will need a letter written by your supervisor on Victoria University letterhead stating your name and that you require a bus ticket in order to travel between campuses for your research. The supervisor will then need to sign the letter with their name typed underneath it. It must also be dated.</p>
            
            <p>When you need a new bus ticket, you must bring this letter along with your used bus ticket.</p>
