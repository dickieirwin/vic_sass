							<h2>Deadlines</h2>

							<p>The Human Ethics Committees operate from 1 February to 30 November each year. Applications received before 1 December each year will be reviewed and applicants notified before the Christmas break. Applications received after 1 December will not be reviewed until February the following year. If an urgent issue arises during January, you should contact the convenor of the appropriate committee.</p>

              <h2 id="special">Special cases</h2>
              
              <h3>Schools of Psychology and Information Management</h3>
              <p>These Schools have their own Human Ethics Subcommitees. If you are a staff member or student in either school, you should apply directly to your School's subcommittee.</p>
              
              <p>The School of Information Management Human Ethics Subcommittee does not close down in December like the other committees. It operates year-round, apart from the University's close-down between Christmas and the New Year.</p>
              
              <h3>Health research</h3>

							<p>Some health research projects must be approved by the Health and Disability Ethics Committees (HDEC), which are operated by the Ministry of Health and accredited by the Health Research Council.</p>
							<p>If you think your project might need HDEC approval, check the Human Ethics Policy and Human Ethics Guidelines, or speak to the Convener of the HEC if you have any questions.</p>
							<p>Examples of projects that need HDEC approval:</p>
							<ul>
								<li>clinical trials</li>
								<li>intervention studies</li>
								<li>projects funded by the Health Research Council</li>
