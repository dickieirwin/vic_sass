<h2></a>What services are included in the Levy?</h2>
<p>The Student Services Levy is used to fund services for students that are not covered by tuition fees, such as counselling, health services, financial support and advice and careers guidance, student advocacy, student publications and student representation.</p>
<p>The services funded by the SSL are:</p>
<h6>Advocacy and legal advice</h6>
<p>This includes independent student advocacy and legal advice services, Student Accommodation advocacy and advice, Student Representation Structure Support, Student consultation</p>
<h6>Careers information, advice and guidance</h6>
<h6>Counselling Services and Pastoral care</h6>
<h6>Employment Information &amp; Advice</h6>
<h6>Assistance and advice to students on financial issues</h6>
<h6>Health Services</h6>
<p>This includes the running of our Medical Centres and Disability Services</li>
<h6>Media</h6>
<p>Supporting the production and dissemination of information by students to students including Salient and VBC</p>
<h6>Early Childhood Services (student crèches)</h6>
<h6>Clubs and societies</h6>
<p>Clubs and Societies administrative, training and funding support; Providing facilities for clubs and societies; Rep group funding and support</p>
<h6>Sports, recreation and cultural activities</h6>
<p>Delivery of sport, recreation and cultural activities; Funding for activities; Recreation Centre; Hub Central Building development as activities venue</p>
