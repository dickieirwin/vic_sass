							<div class='intro'>
								<p>Teaching or research that involves human participants or tissue or affects people’s privacy, rights or freedoms must be approved by the Human Ethics Committee.</p>
							</div>
							
							<p>Research on human subjects at Victoria is governed by the Human Ethics Policy, which sets out the procedures that staff and students must follow. The policy is administered by the Human Ethics Committee, established by the University Council, and subcommittees to which the main committee can delegate its powers.</p>
							
							<h2>Before you apply</h2>
							<p>Your application must be countersigned by your Head of School (or their designee); if you are a student, it must <em>also</em> be countersigned by your supervisor. Before your submit your application, you should discuss the ethical implications of your project with them. You should also check whether your project is covered by any <a href="#special">special cases</a>.</p>
							
							<p>If you are not sure whether your project needs ethics approval, you should check with your Head of School, supervisor, or the convenor of the appropriate Ethics Committee.</p>
             
