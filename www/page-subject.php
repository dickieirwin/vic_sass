                    <h2 id="undergrad">Undergraduate Engineering at Victoria</h2>
                    
                    <p>The Bachelor of Engineering at Victoria University is structured into three parts plus a required practical work experience. Students also choose one of three specialisations.</p>
                    
                    <h6>Part 1</h6>
                    
                    <p>A set of 100-level courses including COMP 102, 103, ENGR 101 and other courses required by the selected specialisation.</p>
                    
                    <h6>Part 2</h6>
                    
                    <p>A set of upper level courses including ENGR 301, 302, 401, 489 and courses required by the selected specialisation.</p>
                    
                    <h6>Part 3</h6>
                    
                    <p>Three approved courses from any Bachelor's degree, including at least one above 100-level and forming a coherent unit of study (eg. three Design courses or three Management courses)</p>
                    
                    <p>Details of each specialisation are available below:</p>
                    
                    <ul>
                      <li><a href="http://www.victoria.ac.nz/home/study/subjects/offered/ecen">Electronic and Computer System Engineering</a></li>
                    
                      <li><a href="http://www.victoria.ac.nz/home/study/subjects/offered/nwen">Network Engineering</a></li>
                    
                      <li><a href="http://www.victoria.ac.nz/home/study/subjects/offered/swen">Software Engineering</a></li>
                    </ul>
