<h2>FAQs</h2>

<h6>Can I borrow the levy on the Student Loan?</h6>

<p>Yes, because it is a compulsory levy.</p>


<h6>Do all students (full-time and part-time) pay the Levy?</h6>

<p>Yes.</p>


<h6>Do distance students pay the Levy?</h6>

<p>Distance learners pay a discounted rate of $338, including GST.</p>


<h6>What about students who are only enrolled in the third trimester?</h6>

<p>Students enrolled in the third trimester only, will be able to claim a two-thirds refund.</p>


<h6>What about students who are only enrolled in the first trimester?</h6>

<p>Students enrolled in the first trimester are eligible to access the services listed below for the year (i.e. February to February).</p>


<h6>Will I receive a refund for the Levy if I withdraw from study?</h6>

<p>If you withdraw from University within the first two weeks of the first term you will get a fees refund and a refund on the Student Services Levy.</p>


<h6>Does the student association still have a role in providing services?</h6>

<p>VUWSA have been contracted to provide some services for the University (including student advocacy, student representation and student media). These are funded from the SSL. VUWSA will provide them to all students, whether the students are members of VUWSA or not.</p>


<h6>Will students still have input into the way their money is being spent?</h6>

<p>The University is committed to ensuring that students still have a say in how their money is spent, and we will work in partnership with student groups, including Student Forum and VUWSA, to ensure consultation occurs. Students may be interested in thereport below that provides details of services provided by the Levy and the related expenditure for 2012 - 2013. This information is intended to provide context for an understanding of the Levy utilisation.</p>

