            <p>Nick Golledge, a senior research fellow with the Antarctic Research Centre, is studying glacier movement in the Transantarctic Mountains in an effort to find out how the East Antarctic Ice Sheet (EAIS) &ndash; the largest reservoir of fresh water on Earth &ndash; is likely to respond to a warming ocean.</p>
            <p>Working with Oliver Marsh, a PhD student from the University of Canterbury, and with support from Antarctica New Zealand field officer Stu Arnold, Golledge installed eight GPS receivers along the length of the Skelton Glacier in early December 2011.</p>
            <p>A helicopter helped the team set up eight solar powered GPS receivers along more than 40 km of the glacier, from the grounding zone near sea level, to 1500 metres high. After a month of recording data &ndash; latitude, longitude and height above sea level &ndash; every 15 seconds, the equipment was retrieved and returned to Christchurch for analysis.</p>
            <p>Large glaciers like the Skelton and the Beardmore &ndash; which Golledge studied in 2010 &ndash; channel ice off the EAIS down to the Ross Ice Shelf. At sea level, the velocity of these glaciers can exceed 300 km per year, but Golledge says his team&rsquo;s research has revealed a &ldquo;significant diurnal variation&rdquo; &ndash; the glaciers accelerate and decelerate in response to the daily tides, as far as 10 km inland from the grounding zone.</p>
            <blockquote>
                &ldquo;This shows the glaciers are very sensitive to what&rsquo;s happening at ocean level.&rdquo;
            </blockquote>
            <p>Golledge has been working on a model of the EAIS 20,000 years ago, during the last glacial maximum. This ice sheet responded to a warming ocean by a period of rapid ice retreat and glacial thinning.</p>
            <p>&ldquo;Our modelling suggests that glaciers accelerate during periods of ocean warming, but slow down again if temperatures stabilise. But by accelerating, the glaciers discharge ice from entire glacier catchments, thinning the ice sheet and reducing its total volume as a consequence.&rdquo;</p>
            <p>Golledge has assessed the 2011 field measurements and says that flow of the Skelton Glacier is clearly influenced by tidal changes in sea level, but how it will respond to a warmer ocean remains unclear. &ldquo;The field measurements we&rsquo;re taking now will help us make more accurate computer models to predict the sort of changes that could happen in the future.&rdquo;</p>
            <p>Golledge&rsquo;s next project is to model the behaviour of the glaciers during the early Pliocene, about 4 million years ago. During this period, global climate was similar to that projected for the near future, but scientists do not yet know if the EAIS grew or shrank during this period.</p>
            <p>If the glaciers of the Transantarctic Mountains accelerate as the ocean warms over coming decades, a reduction in the volume of ice in the EAIS will add fresh water to the ocean and raise global sea levels.</p>
