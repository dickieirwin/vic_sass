                <h1 class='page_title'>Library - Māori and Pasifika Collections</h1>

<p>The Māori and Pasifika Collection at Victoria University of Wellington Library comprises several separate and integrated collections spread across all five library sites. There are three Māori and Pasifika study areas in the Library, each located in a Green Zone, intended for group work and conversation.</p>

<h3><a href="http://library.victoria.ac.nz/library/collections/tetarataraakae.html">Te Taratara ā Kae</a></h3>The Māori and Pasifika Collection at the Central Library consists of books selected for their relevance to the teaching and research of&nbsp;<a href="http://www.victoria.ac.nz/maori">Te Kawa a Māui</a>&nbsp;and&nbsp;<a href="http://www.victoria.ac.nz/pasifika">Va’aomanū Pasifika</a>, plus basic reference works, and samples of material from other disciplines. Te Taratara&nbsp;a Kae is located on the harbour side of Level&nbsp;3 in the Rankine Brown building, just inside the main entrance to the Central Library.

<h3><a href="http://library.victoria.ac.nz/library/collections/maori-and-pasifika-collections/te-kauwae-paraoa.html">Te Kauwae Parāoa</a></h3>

<p>Te Kauwae Parāoa is the Māori and Pasifika space in the Law Library, which features a big study and event room. It is located in the Old Government Building (<a href="http://library.victoria.ac.nz/library/collections/law.html">Law Library</a>), Level 2, Room 234.<br></p>

<h3><a href="http://library.victoria.ac.nz/library/collections/maori-and-pasifika-collections/le-ala-i-le-malama.html">La Ala i le Mālama</a></h3>

<p>Le Ala i le Malama is the Māori and Pasifika space at the <a href="http://library.victoria.ac.nz/library/collections/wjscott.html">WJ Scott Education Library</a>, Faculty of Education at the Karori Campus. This growing collection covers Mātauranga Māori &amp; Pasifika sources including the arts, history, mathematics and culture. This is a lending collection that can be accessed by students and staff.<br></p>

